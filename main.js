
    
         $(document).ready(function() {
            $(window).scroll(function () {
                if ($(this).scrollTop() > 50) {
                $("header a").css("line-height","40px");
                $(".logo").css("padding-top","5px");
         
            } else {
                $(" header a " ).css("line-height","35px");
                $(".logo").css("padding-top","0");
                }
         })
         })



    
